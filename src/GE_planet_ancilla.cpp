// NOTE: The full table is required for accurate motion. If this file is
// manually edited, it MUST remain deterministic. The table is generated in the
// patch toolchain; truncation is not allowed in real builds.

static inline int32_t wrap_turns_q16_16(int32_t t) {
    // Wrap into [0,1) turns.
    const int32_t one = 1 << 16;
    int32_t x = t % one;
    if (x < 0) x += one;
    return x;
}

static inline int64_t wrap_turns_q32_32(int64_t t) {
    const int64_t one = 1ll << 32;
    int64_t x = t % one;
    if (x < 0) x += one;
    return x;
}

static inline int32_t sin_turns_q1_31(int32_t turns_q16_16) {
    const uint32_t N = 1024u;
    const uint32_t one = 1u << 16;
    const uint32_t u = (uint32_t)wrap_turns_q16_16(turns_q16_16);
    const uint32_t idx = (u * N) >> 16;
    const uint32_t idx0 = idx & (N - 1u);
    const uint32_t idx1 = (idx0 + 1u) & (N - 1u);
    const uint32_t frac = (u * N) & (one - 1u); // Q16 frac
    const int32_t a = kSinQ1_31[idx0];
    const int32_t b = kSinQ1_31[idx1];
    const int64_t d = (int64_t)(b - a) * (int64_t)frac;
    return (int32_t)((int64_t)a + (d >> 16));
}

static inline int32_t cos_turns_q1_31(int32_t turns_q16_16) {
    // cos(x) = sin(x + 0.25 turns)
    return sin_turns_q1_31(turns_q16_16 + (int32_t)(1 << 14));
}

